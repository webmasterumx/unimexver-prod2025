            <div class="modal-body" style="color: #337ab7">
                <p>
                    Pre-inscripción en Línea es una herramienta fácil de usar y de alta seguridad, que te brinda la
                    opción de apartar tu lugar en la carrera y horario de interés, apartando una cuota preferencial de
                    Inscripción y con distintas facilidades de pago. <br><br>

                    <b>I. Políticas de Pre-inscripción</b><br>
                    Tu registro en nuestro sistema de Pre-inscripción en Línea contempla las siguientes políticas:
                </p>
                <ol>
                    <li>
                        <b>Plantel:</b> La Pre-inscripción en Línea es válida para estudios en el Plantel Veracruz.
                    </li>
                    <li>
                        <b>Ciclo Escolar:</b> Aplica solamente para alumnos de 1er. Ingreso para el 1er ciclo del Nivel
                        de
                        estudios seleccionado.
                    </li>
                    <li>
                        <b>Periodo Escolar:</b> la Pre-inscripción se realizará para el periodo del año para el que haya
                        inscripciones abiertas y que se especifica en la página de Pre-Inscripción.
                    </li>
                    <li>
                        <b>Nivel y Horarios:</b> Válido para Nivel Licenciatura y/o Posgrado, de acuerdo con los
                        periodos de
                        apertura de inscripciones de cada nivel y los horarios ofertados.
                    </li>
                    <li>
                        <b>Cupo:</b> Con la Pre-inscripción en Línea apartas tu lugar en el Plantel, la Licenciatura o
                        Posgrado
                        y el horario seleccionados, además de la Cuota Preferencial de Inscripción. En caso de no
                        completar la cantidad de alumnos necesaria para la apertura del grupo en el horario
                        seleccionado, la Institución se reserva el derecho de cancelar el grupo, informando al
                        pre-inscrito sobre la decisión, así como las opciones disponibles, en caso de que las hubiera.
                    </li>
                    <li>
                        <b>Vigencia:</b> Tu lugar y cuota quedan reservados durante un periodo de tiempo específico
                        (vigencia)
                        que establece la fecha límite que tienes para pagar el monto total de Inscripción. Después de
                        esa fecha, pierdes tu Cuota Preferencial, teniendo que completar la cuota de Inscripción sin
                        promoción que se fije para dicho Ciclo Escolar. Si no completaste el pago antes del término de
                        la vigencia, no podemos garantizar que habrá cupo en el horario que seleccionaste, así que
                        tendrás que elegir entre los que estén disponibles.
                    </li>
                    <li>
                        <b>Requisitos para concretar la Inscripción:</b><br>
                        <ol type="a">
                            <li>Pagar el monto total de tu Inscripción.</li>
                            <li>Acudir al área de Relaciones del Plantel y completar tu información.</li>
                            <li>
                                Entregar tu documentación completa: <br>
                                <u>Para Licenciatura</u>
                                <ul>
                                    <li>
                                        Acta de Nacimiento (original y copia).
                                    </li>
                                    <li>
                                        Certificado de Bachillerato (original y copia); en caso de que tu documentación
                                        esté en trámite, presenta una Carta de Terminación de Estudios de la Institución
                                        donde cursaste y el Historial Académico (original y copia).
                                    </li>
                                    <li>
                                        2 fotografías tamaño infantil en papel mate con vestimenta formal y nombre de
                                        Alumno escrito al reverso con lápiz.
                                    </li>
                                    <li>
                                        Comprobante (s) de pago de Inscripción, cubriendo el monto total correspondiente
                                        (Ficha de Depósito sellada por el Banco o Comprobante de Pago con Tarjeta de
                                        Crédito).
                                    </li>
                                    <li>
                                        Impresión de correo de confirmación de Pre-inscripción.
                                    </li>
                                    <li>
                                        Acta de Nacimiento Original o Copia Certificada (Original y una Copia). (En caso
                                        de ser Extranjero el Prospecto deberá venir apostillada el Acta de Nacimiento).
                                    </li>
                                    <li>
                                        Certificado de Preparatoria (Original y una Copia) o Carta de Terminación de
                                        Estudios junto con Historial Académico completo.
                                    </li>
                                    <li>
                                        Forma Migratoria vigente con Autorización para realizar estudios en el País.
                                    </li>
                                    <li>
                                        Resolución de Revalidación (únicamente Estudios realizados en el Extranjero).
                                    </li>
                                    <li>
                                        Copia fotostática de la Ficha de Depósito Bancaria Referenciada de Inscripción
                                        con Pago realizado.
                                    </li>
                                    <li>
                                        Documento del CURP.
                                    </li>
                                    <li>
                                        En caso de tener Beca por convenio deberá entregar junto con sus Documentos
                                        necesarios para Inscripción una Carta que certifique la credibilidad de los
                                        Datos.
                                    </li>
                                </ul>
                                <u>Para Posgrado:</u>
                                <ul>
                                    <li>
                                        Copia del Título Profesional o Cédula Profesional. Si aún no lo tienes y quieres
                                        obtenerlo estudiando un Posgrado, presenta una Carta de Autorización para
                                        titularte por créditos de Posgrado, emitida por la Institución en donde cursaste
                                        la Licenciatura.
                                    </li>
                                    <li>
                                        Solicitud de Inscripción requisitada (a mano) y firmada.
                                    </li>
                                    <li>
                                        Comprobante (s) de pago de Inscripción, cubriendo el monto total correspondiente
                                        (Ficha de Depósito sellada por el Banco o Comprobante de Pago con Tarjeta de
                                        Crédito).
                                    </li>
                                    <li>
                                        Certificado Total de Estudios Profesionales (original y copia).
                                    </li>
                                    <li>
                                        Acta de nacimiento (original y copia).
                                    </li>
                                    <li>
                                        Una fotografía tamaño infantil, blanco y negro.
                                    </li>
                                    <li>
                                        Impresión del correo de confirmación de Pre-inscripción.
                                    </li>
                                </ul>
                            </li>
                        </ol>
                    </li>
                    <li>
                        <b>Cuota de Pre-inscripción:</b> Se tomará según el monto vigente en la promoción de Inscripción
                        anticipada. La Pre-inscripción otorga al prospecto una extensión de la fecha límite de pago para
                        respetar la cuota preferencial según la fecha en que se pre-inscribión.
                    </li>
                    <li>
                        <b>Matrícula:</b> Al llenar este formulario quedas Pre-inscrito y generamos una matrícula que
                        usarás
                        durante toda tu trayectoria académica en UNIMEX<sup>®</sup>.
                    </li>
                    <li>
                        <b>Modalidades de Pago:</b> Contempla 3 alternativas para cubrir el monto de la Inscripción.
                        <ol type="a">
                            <li>
                                <u>Pago del total:</u> modalidad de pago en una exhibicion (a cubrir antes de la
                                vigencia
                                establecida).
                            </li>
                            <li>
                                <u>Pago en abonos:</u> modalidad de pago que divide el total en un número de abonos
                                iguales
                                determinado por tu elección (de 2 a 5 abonos), los cuales puedes ir realizando en las
                                fechas que puedas hasta completar el total, antes de la vigencia establecida.
                            </li>
                            <li>
                                <u>Pago en abonos libres:</u> modalidad de pago que te permite elegir libremente el
                                monto de tu
                                primer abono. El saldo pendiente podrás pagarlo libremente, siempre y cuando completes
                                el monto total de tu Inscripción, antes de la fecha límite de vigencia que se estipule
                                durante tu Pre-inscripción.
                            </li>
                        </ol>
                    </li>
                    <li>
                        <b>Formas de pago:</b> Son las diferentes opciones que se te ofrecen para realizar el pago de tu
                        Inscripción.
                        <ol>
                            <li>
                                <u> Pago en ventanilla bancaria de Banamex:</u> Al elegir esta opción, emitiremos una
                                Ficha de
                                Depósito con todos tus datos, para que la imprimas y puedas realizar tu pago en
                                cualquier sucursal Banamex.
                            </li>
                            <li>
                                <u>Tarjeta de crédito (TDC):</u> Puedes realizar tu Pago en Línea directamente desde la
                                página
                                de Pre-inscripción en Línea. Las tarjetas aceptadas son únicamente Visa o MasterCard.
                            </li>
                        </ol>
                    </li>
                    <li>
                        <b>Confirmación:</b> Al término de la Pre-inscripción en Línea se presentará una pantalla de
                        confirmación, en la cual te notificaremos que tu pre-inscripción ha sido exitosa y te
                        informaremos sobre las instrucciones para efectuar el pago (especificando la cuota preferencial
                        que te corresponde y la vigencia) y te explicaremos los pasos para concluir tu proceso de
                        Inscripción. Además, enviaremos a tu correo electrónico una copia de la confirmación, que debe
                        ser impresa y presentada con sus documentos.
                    </li>
                    <li>
                        <b>Datos Personales:</b> Los datos personales que el formulario solicita son necesarios durante
                        el
                        proceso de Inscripción. Al llenarlo te comprometes a proporcionar información verdadera. Tus
                        datos serán utilizados para tu trámite de ingreso a la institución y mantener contacto para
                        orientarte e informarte sobre el status de tu proceso de Inscripción, noticias sobre UNIMEX<sup>®</sup> y
                        promociones o eventos futuros. Por ningún motivo serán compartidos con otras Instituciones, o
                        utilizados con fines diferentes a los aquí establecidos. La información solicitada es:
                        <ol type="a">
                            <li>Nombre (s)</li>
                            <li>Apellido Paterno</li>
                            <li>Apellido Materno</li>
                            <li>Fecha de Nacimiento (dd-mm-aaaa)</li>
                            <li>E-mail</li>
                            <li>Teléfono</li>
                            <li>Domicilio: (Calle / No. / Colonia / Estado)</li>
                        </ol>
                    </li>
                    <li>
                        <b>Tu Elección:</b> Esta sección nos indica tu elección de Pre-inscripción.
                        <ol type="a">
                            <li>
                                Nivel de estudios:
                                <ul>
                                    <li>Licenciatura.</li>
                                    <li>Especialidad.</li>
                                    <li>Maestría.</li>
                                </ul>
                            </li>
                            <li>
                                Plantel
                                <ul>
                                    <li>
                                        Veracruz
                                    </li>
                                </ul>
                            </li>
                            <li>
                                Carrera que desea estudiar (listado de opciones según Nivel y Plantel seleccionados).
                            </li>
                            <li>
                                Horario (listado de horarios según Nivel, Plantel y carrera seleccionados).
                            </li>
                        </ol>
                    </li>
                    <li>
                        <b>Moratorios:</b><u>No se cobrarán cargos moratorios sobre el monto de la Inscripción,</u> en
                        caso de
                        retraso; pero pierdes la cuota preferencial. Además, tu lugar en la Licenciatura o Posgrado y en
                        el horario deseado queda sujeto a disponibilidad.
                    </li>
                </ol>
                <p>
                    <b>II. Politicas de pago</b> <br>
                    A continuación se establecen las Normas de Pago a las que quedará sujeta tu relación como probable
                    alumno de la Universidad:
                </p>
                <ol>
                    <li>
                        Para los alumnos de nuevo ingreso: puedes apartar tu Inscripcion con pagos parciales dentro de
                        los tiempos estipulados, mismos que podran realizarse en el Banco, en linea con Tarjeta de
                        Credito, o en el area de Cajas del Plantel con Tarjeta de Debito o Credito si lo prefieres;
                        siempre y cuando se realicen antes de la fecha limite que indique la Institucion durante tu
                        Pre-inscripcion.
                    </li>
                    <li>
                        Las inscripciones a los cuatrimestres deberán ser realizadas dentro de las fechas expresamente
                        señaladas para tal efecto antes de iniciar el cuatrimestre en cuestión.
                    </li>
                    <li>
                        Si el pago es realizado el mismo día del vencimiento de la fecha límite éste deberá de ser en
                        efectivo o con cheque del mismo Banco o con tarjeta de crédito. SI EL PAGO SE REALIZA CON CHEQUE
                        DE OTRO BANCO, DEBERÁ PAGARSE CON TRES DÍAS DE ANTICIPACIÓN A LA FECHA SEÑALADA .
                    </li>
                    <li>
                        Los pagos efectuados con cheque y que este sea devuelto por el Banco, tendrán un cargo del 30%
                        sobre el importe del mismo.
                    </li>
                    <li>
                        Al ser pre-inscrito eres responsable de la confirmación y cálculo del importe a pagar. UNIMEX<sup>®</sup> te
                        entrega los datos necesarios para el pago total y/o abonos.
                    </li>
                    <li>
                        Una vez devengado el servicio no se reintegra ninguna cuota.
                    </li>
                    <li>
                        La responsabilidad de la Inscripción es 100% del pre-inscrito y debe ser completada totalmente
                        antes de la fecha límite que se establezca, tanto en pago del monto total de Inscripción que le
                        corresponda, como requisitos académicos-escolares.
                    </li>
                </ol>
                <p>
                    <b>III. Politicas de Devoluciones.</b> <br>
                    A continuacion se establecen las Politicas de Devoluciones a las que quedara sujeta tu relacion como
                    probable alumno de la Universidad:
                </p>
                <ol>
                    <li>
                        Este trámite debe realizarse en el área de Cajas del Plantel.
                    </li>
                    <li>
                        Para la devolución deberás de presentar los comprobantes de pago que realizaste (con tarjeta o
                        en el banco) de la Pre-inscripción o Inscripción.
                    </li>
                    <li>
                        Procederá la devolución en caso de que NO se abra el grupo y horario solicitado.
                    </li>
                    <li>
                        Una vez iniciado el ciclo escolar y tomada alguna clase, no hay devolución del monto de
                        Inscripción.
                    </li>
                </ol>
