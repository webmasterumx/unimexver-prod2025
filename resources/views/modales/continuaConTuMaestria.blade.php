                <b>
                    3. Puedes aplicar la Titulación vía Estudios de Posgrado.
                </b>
                <ol type="a">
                    <li>
                        <b> Titulación de Licenciatura vía estudios de Posgrado:</b> en caso de haber elegido esta
                        opción de
                        Titulación, al concluir el 100% de los créditos de la Especialidad puedes iniciar el trámite
                        para recibir tu Título de Licenciatura en la Institución donde la cursaste.
                        <br>
                        <br>
                        Consulta los requisitos en la sección “Titulación de vía estudios de Posgrado”.
                    </li>
                    <li>
                        <b>Titulación de Especialidad vía estudios de Maestría:</b> elige esta opción de titulación para
                        obtener tu Diploma de Especialidad, aplicando tu equivalencia de materias y cursando únicamente
                        los últimos dos ciclos de la Maestría correspondiente.
                        <br>
                        <br>
                        Consulta los requisitos en la sección “Titulación de vía estudios de Posgrado”.
                    </li>
                </ol>
                <b>
                    4. Consideraciones:
                </b>
                <ol type="a">
                    <li>
                        La inscripción está sujeta a cupo en el grupo seleccionado. La apertura de los grupos está
                        sujeta a la disponibilidad del programa en el plantel deseado y a un mínimo de 25 alumnos
                        inscritos por grupo, previo al inicio de clases. Consulta los programas que se impartirán en el
                        ciclo de tu interés y en el plantel deseado.
                    </li>
                    <li>
                        Si el programa que solicitas no se imparte en tu plantel para el ciclo escolar deseado, podrás:
                        <ul>
                            <li>
                                Solicitar tu cambio a otro plantel UNIMEX<sup>®</sup> que cuente con disponibilidad en el grupo
                                deseado.
                            </li>
                            <li>
                                Anotarte para que te informemos de posibles aperturas en ciclos escolares futuros.
                            </li>
                        </ul>
                    </li>
                    <li>
                        En caso de que hubieras realizado el pago de tu inscripción y tu grupo no se haya formado,
                        puedes aplicar el monto pagado de tu en otro plantel y/o en otro ciclo en el que se imparta el
                        programa o solicitar tu reembolso en la Ventanilla única de tu Plantel.
                    </li>
                </ol>
